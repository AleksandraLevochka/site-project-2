let recipeList = document.getElementById("recipe_container");

// Get reference to the right split container
let rightSplit = document.querySelector('.split.right');

// Function to update the content of the right split
function updateRightSplitContent(item) {
    // Clear previous content
    rightSplit.innerHTML = '';

    // Create elements for the image and information
    let image = document.createElement('img');
    image.src = item.item;
    image.classList.add('img');
    rightSplit.appendChild(image);

    let details = document.createElement('div');
    details.classList.add('details');

    let name = document.createElement('h2');
    name.innerText = item.name;
    details.appendChild(name);

    let preparationTime = document.createElement('p');
    preparationTime.innerText = 'Preparation Time: ' + item.preparation_time;
    details.appendChild(preparationTime);

    let difficultyLevel = document.createElement('p');
    difficultyLevel.innerText = 'Difficulty Level: ' + item.difficulty_level;
    details.appendChild(difficultyLevel);

    let mealType = document.createElement('p');
    mealType.innerText = 'Meal Type: ' + item.meal_type;
    details.appendChild(mealType);

    let ingredientsHeading = document.createElement('h3');
    ingredientsHeading.innerText = 'Ingredients';
    details.appendChild(ingredientsHeading);

    let ingredientsList = document.createElement('ul');
    item.ingredients.forEach(ingredient => {
        let listItem = document.createElement('li');
        listItem.innerText = ingredient;
        ingredientsList.appendChild(listItem);
    });
    details.appendChild(ingredientsList);

    let instructionsHeading = document.createElement('h3');
    instructionsHeading.innerText = 'Instructions';
    details.appendChild(instructionsHeading);

    let instructions = document.createElement('p');
    instructions.innerText = item.instructions;
    details.appendChild(instructions);

    rightSplit.appendChild(details);
}

// Iterate over the collection and add hover event listeners
collection.forEach(item => {
    let listItem = document.createElement("div");
    listItem.innerText = item.name;
    listItem.classList.add("recipe-item");
    listItem.addEventListener('mouseover', () => {
        updateRightSplitContent(item);
    });
    recipeList.appendChild(listItem);
});






